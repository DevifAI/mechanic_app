import axios, {
  AxiosError,
  AxiosInstance,
  AxiosRequestConfig,
  AxiosResponse,
  InternalAxiosRequestConfig,
} from 'axios';
import {enviornment, URLs} from './api.enviornment';
import { Alert } from 'react-native';
import { forceLogout } from '../utils/forceLogout';
import { store } from '../redux/store';

// Base Axios configuration
const axiosConfig: AxiosRequestConfig = {
  baseURL: URLs[enviornment].apiURL,
  headers: {
    'Content-Type': 'application/json',
  },
};

const axiosAuthConfig: AxiosRequestConfig = {
  baseURL: URLs[enviornment].apiURL,
  headers: {
    'Content-Type': 'application/json',
  },
};

// Create instances
export const baseClient: AxiosInstance = axios.create(axiosConfig);
export const authClient: AxiosInstance = axios.create(axiosAuthConfig);

// Logging helpers
const logRequest = (config: AxiosRequestConfig) => {
  console.log('üì§ [REQUEST]', {
    url: config.url,
    method: config.method,
    headers: config.headers,
    data: config.data,
  });
  return config;
};

const logResponse = (response: any) => {
  console.log('‚úÖ [RESPONSE]', {
    url: response.config?.url,
    status: response.status,
    data: response.data,
  });
  return response;
};

const logError = (error: any) => {
  if (error.response) {
    console.error('‚ùå [RESPONSE ERROR]', {
      url: error.config?.url,
      status: error.response.status,
      data: error.response.data,
    });
  } else if (error.request) {
    console.error('‚ö†Ô∏è [NO RESPONSE]', error.request);
  } else {
    console.error('üö® [AXIOS ERROR]', error.message);
  }
  return Promise.reject(error);
};

// Apply interceptors to a client
const applyInterceptors = (
  client: AxiosInstance,
  withAuth: boolean = false,
) => {
  client.interceptors.request.use(
    (config: InternalAxiosRequestConfig): InternalAxiosRequestConfig => {
      if (withAuth) {
        const accessToken = store.getState().auth.token; // Add real token logic here
        if (accessToken) {
          config.headers.Authorization = `Bearer ${accessToken}`;
        }
      }

      // Logging
      console.log('üì§ [REQUEST]');
      console.log(`‚û°Ô∏è URL: ${config.baseURL}${config.url}`);
      console.log(`üîÅ Method: ${config.method?.toUpperCase()}`);
      console.log('üßæ Headers:', config.headers);
      if (config.data) {
        console.log('üì¶ Payload:', config.data);
      }

      return config;
    },
    (error: AxiosError) => {
      console.error('üõë [REQUEST ERROR]', error);
      return Promise.reject(error);
    },
  );

  client.interceptors.response.use(
    (response: AxiosResponse) => {
      console.log('‚úÖ [RESPONSE]');
      console.log(`‚¨ÖÔ∏è URL: ${response.config.baseURL}${response.config.url}`);
      console.log(`üìä Status: ${response.status}`);
      console.log('üì• Data:', response.data);
      return response;
    },
    (error: AxiosError) => {
      if (error.response) {
        console.error('‚ùå [ERROR RESPONSE]');
        console.error(`URL: ${error.config?.baseURL}${error.config?.url}`);
        console.error(`Status: ${error.response.status}`);
        console.error('Error Data:', error.response.data);
          if (error.response.status === 401) {
        Alert.alert(
          'Session Expired',
          'Your session has expired. Please login again.',
          [
            {
              text: 'OK',
              onPress: () => {
                forceLogout();
              },
            },
          ],
          { cancelable: false },
        );
      }
      } else if (error.request) {
        console.error('‚ö†Ô∏è [NO RESPONSE]');
        console.error('Request:', error.request);
      } else {
        console.error('üö® [REQUEST SETUP ERROR]');
        console.error('Message:', error.message);
      }
      return Promise.reject(error);
    },
  );
};

// Apply interceptors to both clients
applyInterceptors(baseClient , true);
applyInterceptors(authClient, true);

export default {baseClient, authClient};
